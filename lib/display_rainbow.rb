def display_rainbow(array)
  print "#{array[0][0].upcase}: #{array[0]}, #{array[1][0].upcase}: #{array[1]}, "
  print "#{array[2][0].upcase}: #{array[2]}, #{array[3][0].upcase}: #{array[3]}, "
  print "#{array[4][0].upcase}: #{array[4]}, #{array[5][0].upcase}: #{array[5]}, "
  puts "#{array[6][0].upcase}: #{array[6]}"
end
