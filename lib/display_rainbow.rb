def display_rainbow(color)
  red = ("R: #{color[0]}");
  orange = ("O: #{color[1]}");
  yellow = ("Y: #{color[2]}");
  green = ("G: #{color[3]}");
  blue = ("B: #{color[4]}");
  indigo = ("I: #{color[5]}");
  violet = ("V: #{color[6]}");
  print(red, ", ", orange, ", ", yellow, ", ", green, ", ", blue, ", ", indigo, ", ", violet, "\n");
end
